/// Fibonacci 數列 是什麼?
/// 2 3 5 7 9 11 13 17 19 23 29 31 質數
/// 1 3 5 7 9 11 13 15 17 奇數
/// 2 4 6 8 10 12 14 16  偶數
/// 1 1 2 3 5 8 13 21 34 請問下一個數字是什麼
/// 規則: 前2項相加
#include <stdio.h>
int main()
{/// 陣列的宣告, 右邊有它的值, 沒寫的都放0
   int a[30] = {0, 1}; /// 裡面放答案
   printf("1 "); /// 先印出第1項
   for(int i=2; i<30; i++){ /// 再印後面的項
       a[i] = a[i-1] +a[i-2]; /// 前2項相加
       printf("%d",a[i] );
   }
}
